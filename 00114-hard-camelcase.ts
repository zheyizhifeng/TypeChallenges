type StartWithLetter<S extends string> = S extends `${infer _ extends Letters}${string}`
  ? true
  : false;
  
type CamelCase<S extends string> = S extends `${infer Word1}_${infer Word2}`
  ? StartWithLetter<Word2> extends true
    ? `${Lowercase<Word1>}${Capitalize<CamelCase<Word2>>}`
    : `${Lowercase<Word1>}_${Capitalize<CamelCase<Word2>>}`
  : Lowercase<S>;
